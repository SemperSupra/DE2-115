import cv2
import numpy as np

# Font from vga_text_console.v
def get_font_row(ch, row):
    r = row // 2
    if ch == ord(' '): return 0x00
    if ch == ord('-'): return 0x7c if r == 3 else 0x00
    if ch == ord(':'): return 0x18 if r == 2 or r == 5 else 0x00
    if ch == ord('_'): return 0x7e if r == 7 else 0x00
    
    digits = {
        '0': [0x3c, 0x66, 0x6e, 0x76, 0x66, 0x66, 0x3c],
        '1': [0x18, 0x38, 0x18, 0x18, 0x18, 0x18, 0x7e],
        '2': [0x3c, 0x66, 0x06, 0x1c, 0x30, 0x60, 0x7e],
        '3': [0x3c, 0x66, 0x06, 0x1c, 0x06, 0x66, 0x3c],
        '4': [0x0c, 0x1c, 0x3c, 0x6c, 0x7e, 0x0c, 0x0c],
        '5': [0x7e, 0x60, 0x7c, 0x06, 0x06, 0x66, 0x3c],
        '6': [0x1c, 0x30, 0x60, 0x7c, 0x66, 0x66, 0x3c],
        '7': [0x7e, 0x06, 0x0c, 0x18, 0x30, 0x30, 0x30],
        '8': [0x3c, 0x66, 0x66, 0x3c, 0x66, 0x66, 0x3c],
        '9': [0x3c, 0x66, 0x66, 0x3e, 0x06, 0x0c, 0x38],
    }
    if chr(ch) in digits:
        rows = digits[chr(ch)]
        return rows[r] if r < len(rows) else 0x00
    
    letters = {
        'A': [0x18, 0x3c, 0x66, 0x66, 0x7e, 0x66, 0x66],
        'B': [0x7c, 0x66, 0x66, 0x7c, 0x66, 0x66, 0x7c],
        'C': [0x3c, 0x66, 0x60, 0x60, 0x60, 0x66, 0x3c],
        'D': [0x78, 0x6c, 0x66, 0x66, 0x66, 0x6c, 0x78],
        'E': [0x7e, 0x60, 0x60, 0x7c, 0x60, 0x60, 0x7e],
        'F': [0x7e, 0x60, 0x60, 0x7c, 0x60, 0x60, 0x60],
        'G': [0x3c, 0x66, 0x60, 0x6e, 0x66, 0x66, 0x3c],
        'H': [0x66, 0x66, 0x66, 0x7e, 0x66, 0x66, 0x66],
        'I': [0x3c, 0x18, 0x18, 0x18, 0x18, 0x18, 0x3c],
        'J': [0x1e, 0x0c, 0x0c, 0x0c, 0x0c, 0x6c, 0x38],
        'K': [0x66, 0x6c, 0x78, 0x70, 0x78, 0x6c, 0x66],
        'L': [0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0x7e],
        'M': [0x63, 0x77, 0x7f, 0x6b, 0x63, 0x63, 0x63],
        'N': [0x66, 0x76, 0x7e, 0x7e, 0x6e, 0x66, 0x66],
        'O': [0x3c, 0x66, 0x66, 0x66, 0x66, 0x66, 0x3c],
        'P': [0x7c, 0x66, 0x66, 0x7c, 0x60, 0x60, 0x60],
        'Q': [0x3c, 0x66, 0x66, 0x66, 0x6e, 0x3c, 0x0e],
        'R': [0x7c, 0x66, 0x66, 0x7c, 0x78, 0x6c, 0x66],
        'S': [0x3c, 0x66, 0x60, 0x3c, 0x06, 0x66, 0x3c],
        'T': [0x7e, 0x18, 0x18, 0x18, 0x18, 0x18, 0x18],
        'U': [0x66, 0x66, 0x66, 0x66, 0x66, 0x66, 0x3c],
        'V': [0x66, 0x66, 0x66, 0x66, 0x66, 0x3c, 0x18],
        'W': [0x63, 0x63, 0x63, 0x6b, 0x7f, 0x77, 0x63],
        'X': [0x66, 0x66, 0x3c, 0x18, 0x3c, 0x66, 0x66],
        'Y': [0x66, 0x66, 0x66, 0x3c, 0x18, 0x18, 0x18],
        'Z': [0x7e, 0x06, 0x0c, 0x18, 0x30, 0x60, 0x7e],
    }
    c = chr(ch).upper()
    if c in letters:
        rows = letters[c]
        return rows[r] if r < len(rows) else 0x00
    
    return 0x00

def get_char_template(ch):
    tpl = np.zeros((16, 8), dtype=np.uint8)
    for r in range(16):
        row_bits = get_font_row(ch, r)
        for c in range(8):
            if row_bits & (1 << (7 - c)):
                tpl[r, c] = 255
    return tpl

chars = " 0123456789ABCDEFGHIKLMNOPQRSTUVWXYZ:/-_|."
templates = {c: get_char_template(ord(c)) for c in chars}

def read_vga_text(filename, row_start, row_end):
    img = cv2.imread(filename, cv2.IMREAD_GRAYSCALE)
    if img is None: return []
    _, thresh = cv2.threshold(img, 128, 255, cv2.THRESH_BINARY)
    contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not contours: return []
    main_box = max(contours, key=cv2.contourArea)
    bx, by, bw, bh = cv2.boundingRect(main_box)
    cw = bw / 80
    ch = bh / 30
    results = []
    for r in range(row_start, row_end):
        line = ""
        for c in range(80):
            x1 = int(bx + c * cw)
            y1 = int(by + r * ch)
            x2 = int(bx + (c+1) * cw)
            y2 = int(by + (r+1) * ch)
            char_crop = thresh[y1:y2, x1:x2]
            char_crop = cv2.resize(char_crop, (8, 16))
            best_char = '?'
            max_sim = -1
            for char, tpl in templates.items():
                sim = np.sum(char_crop == tpl)
                if sim > max_sim:
                    max_sim = sim
                    best_char = char
            if max_sim < (8*16*0.7): line += "?"
            else: line += best_char
        results.append(line)
    return results

def main():
    print("Decoding VGA Text:")
    lines = read_vga_text('vga_capture.jpg', 0, 29)
    for i, l in enumerate(lines):
        print(f"Row {i:2}: |{l}|")

if __name__ == "__main__":
    main()
